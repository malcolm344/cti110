Circumferance = math.pi * radius * 2
area = math.pi * (radius ** 2) 


print(f'\nThe diameter of the circe is {diameter:.1f}')
print(f'\nThe circumfrance of the circle is {Circumferance:.2f}')
print(f'\nThe area of the circle is {area:.3f}')               
               

